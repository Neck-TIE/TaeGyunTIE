package tie.inflearn.java_introduce.hello;

// HelloJava 클래스 파일의 범위 시작
public class HelloJava {

    // main 메서드, 프로그램의 시작점
    public static void main(String[] args) {
        // 값을 콘솔에 출력해주는 기능
        // 자바는 문자열을 사용할 때 "" 쌍따옴표를 사용
        System.out.println("Hello Java");
    } // main 메서드의 범위 끝

} // HelloJava 클래스의 범위 끝