// 파일명과 클래스 이름이 같아야 한다.
public class HelloJava { // HelloJava 클래스 파일의 범위 시작
    public static void main(String[] args) { // main 메서드, 프로그램의 시작점
        // 값을 콘솔에 출력해주는 기능
        System.out.println("Hello Java"); // 자바는 문자열을 사용할 때 "" 쌍따옴표를 사용
    } // main 메서드의 범위 끝
} // HelloJava 클래스의 범위 끝

// 자바 언어는 대소문자를 구분한다. 대소문자가 다르면 오류가 발생할 수 있다.