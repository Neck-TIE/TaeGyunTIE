package variable;

public class Var2 {

    public static void main(String[] args) {
        int a; // 변수 선언
        // a = 10;
        a = 20; // 변수 초기화, 10 -> 20

        // 변수 값 읽기
        System.out.println(a); // 20
        System.out.println(a); // 20
        System.out.println(a); // 20
    }

}